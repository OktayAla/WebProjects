                <h2>Tarihçe</h2>
                <p>Afrodisias Antik Kenti, MÖ 6. yüzyılda kurulmuş olup, adını aşk ve güzellik tanrıçası Afrodit'ten almıştır. Kent, Roma döneminde önemli bir sanat ve heykel merkezi haline gelmiş, özellikle mermer ocakları sayesinde zenginleşmiştir. Afrodisias'ta üretilen heykeller, Roma İmparatorluğu'nun dört bir yanına ihraç edilmiştir.</p>
                <p>Kentin Afrodit kültü ile ilişkisi, kuruluşundan çok daha eskilere dayanmaktadır. Bölgede yapılan arkeolojik araştırmalar, MÖ 3. binyıla ait yerleşim izlerini ve ana tanrıça kültüne ait buluntuları ortaya çıkarmıştır. Bu durum, Afrodisias'ın sadece bir Roma kenti olmadığını, aynı zamanda çok daha eski bir kültürel mirasa sahip olduğunu göstermektedir.</p>
                <p>Roma İmparatorluğu döneminde, özellikle Augustus ve Hadrianus dönemlerinde, Afrodisias büyük bir gelişme göstermiştir. İmparatorlar, kente özel önem vermişler ve birçok yeni yapı inşa ettirmişlerdir. Bu dönemde, Afrodisias'ın nüfusu önemli ölçüde artmış ve kent, bölgenin en önemli merkezlerinden biri haline gelmiştir.</p>
                <p>Afrodisias'ın heykeltraşlık okulu, antik dünyanın en ünlü ve saygın okullarından biriydi. Kentte üretilen heykeller, yüksek kaliteleri ve sanatsal değerleriyle ün kazanmıştır. Afrodisiaslı heykeltraşlar, sadece yerel pazarda değil, aynı zamanda Roma, Atina ve diğer önemli kentlerde de büyük talep görmüşlerdir.</p>
                <p>Bizans İmparatorluğu döneminde Afrodisias, Hristiyanlık merkezi haline gelmiş ve kentte birçok kilise inşa edilmiştir. Ancak, bu dönemde kent, depremler ve savaşlar nedeniyle zaman zaman tahrip olmuştur.</p>
                <p>Türklerin Anadolu'ya gelmesiyle birlikte Afrodisias, Türk egemenliğine girmiştir. Kent, bu dönemde Geyre adıyla anılmaya başlanmış ve zamanla önemini kaybetmiştir. Ancak, antik kentin kalıntıları, günümüze kadar ulaşmayı başarmıştır.</p>

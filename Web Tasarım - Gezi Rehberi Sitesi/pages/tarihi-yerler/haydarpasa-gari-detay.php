                <p>Haydarpaşa Garı, Osmanlı İmparatorluğu döneminde, 1909 yılında hizmete açılmıştır. Alman mimarlar Otto Ritter ve Helmut Cuno tarafından tasarlanan gar, İstanbul'u Bağdat ve Hicaz'a bağlayan demiryolu hattının başlangıç noktası olarak inşa edilmiştir.</p>

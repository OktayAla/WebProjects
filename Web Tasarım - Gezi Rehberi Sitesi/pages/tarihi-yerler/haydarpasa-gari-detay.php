                <p>Haydarpaşa Garı, I. Dünya Savaşı ve Kurtuluş Savaşı sırasında askeri amaçlarla kullanılmıştır. Gar, bu dönemde önemli bir lojistik merkez olarak görev yapmıştır.</p>
                <p>Gar binası, 2010 yılında çıkan bir yangın sonucu büyük hasar görmüştür. Yangın, binanın çatısında ve iç kısımlarında büyük hasara yol açmıştır.</p>
                <p>Günümüzde Haydarpaşa Garı, restore edilerek yeniden kullanıma açılması planlanmaktadır. Garın, kültürel etkinliklere ev sahipliği yapması ve bir müze olarak hizmet vermesi düşünülmektedir.</p>
                <p>Haydarpaşa Garı, İstanbul'un tarihi ve kültürel mirasının önemli bir parçasıdır ve korunması gereken bir yapıdır.</p>

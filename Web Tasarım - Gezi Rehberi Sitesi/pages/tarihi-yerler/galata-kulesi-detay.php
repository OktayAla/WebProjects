                <h2>Tarihçe</h2>
                <p>Galata Kulesi, Bizans ve Ceneviz dönemlerinin izlerini taşıyan, 1348 yılında inşa edilmiş tarihi bir kuledir. İstanbul'un eski surlarını ve şehrin kalabalık yaşamını gözler önüne seren bu yapı, zaman içinde yangınlar ve depremler gibi zorlukları atlatmış, restore edilerek günümüze ulaşmıştır.</p>
                <p>Kule, ilk inşa edildiğinde bir deniz feneri ve gözetleme kulesi olarak kullanılmıştır. Amacı, Haliç'e ve Boğaz'a giren gemileri kontrol altında tutmak ve şehri olası saldırılara karşı uyarmaktı.</p>
                <p>Osmanlı döneminde ise kule, zindan olarak da kullanılmış, daha sonra yangınları haber vermek amacıyla yangın kulesi olarak görev yapmıştır. Bu dönemde, kulenin tepesinde sürekli bir nöbetçi bulunurdu ve yangın çıktığında alarm verilirdi.</p>
                <p>17. yüzyılda Hezarfen Ahmet Çelebi'nin tahtadan kanatlarla Galata Kulesi'nden Üsküdar'a uçtuğu efsanesi, kulenin tarihinde önemli bir yer tutar. Bu olay, kulenin cesaret ve maceraperestlikle özdeşleşmesine katkıda bulunmuştur.</p>
                <p>Günümüzde Galata Kulesi, İstanbul'un en popüler turistik mekanlarından biridir. Ziyaretçiler, kulenin tepesinden eşsiz İstanbul manzarasının tadını çıkarabilir, kulenin tarihi atmosferini deneyimleyebilir ve çevresindeki tarihi Galata semtini keşfedebilirler.</p>

                <h2>Tarihçe</h2>
                <p>Eşrefoğlu Camii, 1297-1299 yılları arasında Eşrefoğlu Süleyman Bey tarafından yaptırılmıştır. Anadolu'nun en büyük ahşap direkli camisi olma özelliğini taşır ve Selçuklu ahşap işçiliğinin en güzel örneklerinden biridir. Caminin yapımında kullanılan sedir ağaçları, Beyşehir Gölü'nden getirilmiştir.</p>
                <p>Eşrefoğulları Beyliği döneminde inşa edilen cami, beyliğin gücünü ve zenginliğini simgelemektedir.</p>
                <p>Caminin yapımında, dönemin en yetenekli ustaları görev almıştır. Ahşap işçiliği, taş oymacılığı ve çini sanatında uzmanlaşmış zanaatkarlar, caminin her köşesini özenle işlemişlerdir.</p>
                <p>Caminin minberi, ceviz ağacından yapılmış olup, kündekari tekniği ile süslenmiştir. Bu minber, Selçuklu ahşap sanatının şaheserlerinden biri olarak kabul edilmektedir.</p>
                <p>Caminin tavanı, ahşap işçiliğinin en güzel örneklerini sergilemektedir. Tavanda yer alan geometrik desenler ve bitkisel motifler, camiye ayrı bir güzellik katmaktadır.</p>
                <p>Günümüzde Eşrefoğlu Camii, hem bir ibadethane hem de bir müze olarak ziyaretçilerini ağırlamaktadır. Cami, Selçuklu sanatının ve mimarisinin önemli bir temsilcisi olarak korunmaya devam etmektedir.</p>

            <section class="history-section">
                <h2>Tarihçe</h2>
                <p>Nemrut Dağı, Kommagene Kralı I. Antiochus Theos tarafından MÖ 62 civarında inşa ettirilmiştir. Zirvede yer alan anıt mezar tapınağı, tanrı ve krallık figürlerini betimleyen devasa heykelleriyle antik dünyanın en dikkat çekici yapılarından biridir.</p>
                <p>I. Antiochus, hem Pers hem de Makedon kökenli bir soydan gelmekteydi ve bu durum, Nemrut Dağı'ndaki heykellerde ve kabartmalarda kendini göstermektedir. Kral, anıt mezarı yaptırarak kendi tanrısal statüsünü vurgulamak ve krallığının gücünü simgelemek istemiştir.</p>
                <p>Dağın zirvesindeki anıt mezar kompleksi, doğu ve batı terasları, heykeller, kabartmalar ve yazıtlarla doludur. Heykeller, Zeus-Oromasdes (Roma tanrısı Jüpiter ve Pers tanrısı Ahura Mazda'nın birleşimi), Apollon-Mithras-Helios-Hermes, Herakles-Ares-Artagnes ve Kommagene'nin koruyucu tanrıçası gibi figürleri temsil etmektedir.</p>
                <p>Nemrut Dağı, 1881 yılında Alman mühendis Karl Sester tarafından yeniden keşfedilmiştir. Daha sonra yapılan arkeolojik kazılar, anıt mezarın ve heykellerin önemini ortaya çıkarmış ve bölgeyi dünya çapında bir ilgi odağı haline getirmiştir.</p>
                <p>Günümüzde Nemrut Dağı, Türkiye'nin en önemli turistik yerlerinden biridir. Her yıl binlerce turist, dağın zirvesine çıkarak gün doğumunu ve batımını izlemekte ve antik dünyanın bu eşsiz mirasına tanıklık etmektedir.</p>
            </section>


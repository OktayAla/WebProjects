                <h2>Hakkında</h2>
                <p>Olimpos, Antalya'nın Kumluca ilçesi yakınlarında, Beydağları Milli Parkı içerisinde yer alan antik bir Likya kentidir. Kent, MÖ 3. yüzyılda kurulmuş olup, Likya Birliği'nin önemli liman kentlerinden biri olmuştur. Tarih boyunca korsanların da üssü olarak kullanılmıştır.</p>

                <p>Olimpos, adını yakındaki Tahtalı Dağı'ndan (antik dönemdeki adı Olimpos) almıştır. Kent, Bizans döneminde de önemini korumuş, ancak daha sonra terk edilmiştir. Günümüzde, antik kentin kalıntıları, yemyeşil orman örtüsü ve denize akan bir derenin ortasında yer almaktadır. Kentin en önemli kalıntıları arasında Roma Hamamı, tiyatro, bazilika ve kaya mezarları bulunmaktadır.</p>

                <p>Olimpos, tarihi kalıntılarının yanı sıra, doğal güzellikleriyle de ünlüdür. Olimpos Plajı, Türkiye'nin en güzel plajlarından biri olarak kabul edilmektedir. Ayrıca, bölgede kamp yapmak, trekking yapmak ve kaya tırmanışı yapmak gibi aktiviteler de popülerdir.</p>

                <p>Olimpos'un yakınında bulunan Çıralı Plajı'nda, antik çağlardan beri yanan doğal gaz kaynakları olan Yanartaş (Chimera) bulunmaktadır.</p>

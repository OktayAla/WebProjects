                <h2>Hakkında</h2>
                <p>Düden Şelalesi, Antalya'da yer alan ve aynı adı taşıyan iki farklı şelaleden oluşmaktadır: Yukarı Düden Şelalesi ve Aşağı Düden Şelalesi. Her iki şelale de Düden Çayı üzerinde bulunmaktadır ve Antalya'nın önemli doğal güzelliklerinden biridir.</p>

                <p><b>Yukarı Düden Şelalesi (Düdenbaşı Şelalesi):</b> Antalya şehir merkezine yaklaşık 10 km uzaklıkta, Kepez ilçesi sınırları içerisinde yer alır. Doğal bir park alanı içerisinde bulunur ve ziyaretçilerine piknik yapma, dinlenme ve doğayla iç içe olma imkanı sunar. Şelalenin kaynağı karstik yapılı dağlardan gelen sularla beslenir. Şelale, bir mağara içinden geçerek farklı kollardan dökülür, bu da ona ayrı bir güzellik katar.</p>

                <p><b>Aşağı Düden Şelalesi (Karpuzkaldıran Şelalesi):</b> Antalya şehir merkezine yaklaşık 8 km uzaklıkta, Lara semtinde yer alır. Şelalenin en dikkat çekici özelliği, yaklaşık 40 metre yükseklikten Akdeniz'e dökülmesidir. Bu görkemli manzara, özellikle tekne turları ile denizin üzerinden izlendiğinde daha da etkileyici bir deneyim sunar.</p>

                <p>Düden Şelalesi, Antalya'yı ziyaret eden yerli ve yabancı turistlerin uğrak noktalarından biridir. Şelalelerin çevresinde restoranlar, kafeler ve hediyelik eşya dükkanları bulunmaktadır.</p>

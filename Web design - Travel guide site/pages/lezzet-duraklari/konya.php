                        <li>Tüm malzemeleri iyice karıştırın ve buzdolabında 30 dakika dinlendirin.</li>
                        <li>Mayalanan hamuru 4 eşit parçaya bölün ve her birini unlanmış zeminde çok ince açın.</li>
                        <li>Açtığınız hamurun üzerine hazırladığınız iç harcı ince bir tabaka halinde yayın.</li>

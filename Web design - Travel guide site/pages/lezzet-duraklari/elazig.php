                    <h4>Hazırlanışı</h4>
                    <ol>
                        <li>Soğanları çok ince doğrayın veya rendeleyin.</li>
                        <li>Maydanozu ince kıyın.</li>
                        <li>Cevizleri iri parçalar halinde kırın.</li>
                        <li>Geniş bir kapta kıyma, soğan, bulgur, maydanoz, tuz ve baharatları karıştırın.</li>
                        <li>Karışımı iyice yoğurun, en az 15-20 dakika yoğurma işlemi yapın.</li>
                        <li>Yoğurduğunuz karışımdan ceviz büyüklüğünde parçalar koparın.</li>
                        <li>Her parçanın içine bir parça ceviz koyun ve yuvarlak şekil verin.</li>
                        <li>Hazırladığınız köfteleri yağlanmış fırın tepsisine dizin.</li>
                        <li>Sos için, tereyağını eritin, salçaları ekleyip kavurun.</li>
                        <li>Ezilmiş sarımsak ve sıcak suyu ekleyip karıştırın.</li>
                        <li>Hazırladığınız sosu köftelerin üzerine gezdirin.</li>
                        <li>Önceden ısıtılmış 180 derece fırında 30-35 dakika pişirin.</li>
                        <li>Pişen köfteleri sıcak servis yapın.</li>

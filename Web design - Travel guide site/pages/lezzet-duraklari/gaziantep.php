                <div class="preparation-steps">
                    <h4>Hazırlanışı</h4>
                    <ol>
                        <li>Şerbeti hazırlamak için şeker ve suyu bir tencereye alıp kaynatın. Kaynamaya başlayınca limon suyunu ekleyin ve 15 dakika daha kaynatıp soğumaya bırakın.</li>
                        <li>Hamur için un, yumurta, su, tuz ve sirkeyi bir kapta yoğurun. Sert bir hamur elde edin.</li>
                        <li>Hamuru 30-35 bezeye ayırın ve üzerini nemli bir bezle örtüp 30 dakika dinlendirin.</li>
                        <li>Her bezeyi çok ince açın. Açarken aralara nişasta serpin ki yapışmasın.</li>
                        <li>Tereyağını eritin ve süzün.</li>
                        <li>Fırın tepsisine ilk yufkayı yerleştirin ve üzerine eritilmiş tereyağı sürün.</li>
                        <li>Bu işlemi 15 kat yufka için tekrarlayın.</li>
                        <li>15. katın üzerine çekilmiş Antep fıstığını serpin.</li>
                        <li>Kalan yufkaları da aynı şekilde tereyağı sürerek üst üste dizin.</li>
                        <li>En üste tereyağı sürdükten sonra, baklavayı dilimleyin.</li>
                        <li>Kalan tereyağını baklavanın üzerine gezdirin.</li>
                        <li>Önceden ısıtılmış 180 derece fırında altı ve üstü kızarana kadar pişirin (yaklaşık 30-35 dakika).</li>
                        <li>Fırından çıkan sıcak baklavanın üzerine soğuk şerbeti dökün.</li>
                        <li>Şerbetini çekmesi için 3-4 saat bekletin.</li>
                        <li>Servis yapmadan önce üzerine çekilmiş Antep fıstığı serpin.</li>
                    </ol>

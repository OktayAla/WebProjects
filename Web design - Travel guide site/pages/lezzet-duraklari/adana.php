                    <li>İçine kuyruk yağı, kırmızı pul biber, tuz ve bazı baharatlar eklenir.</li>
                    <li>Şişlere özel bir teknikle dizilir ve mangalda pişirilir.</li>
                    <li>Yanında genellikle közlenmiş domates, biber, soğan ve lavaş ekmeği ile servis edilir.</li>
